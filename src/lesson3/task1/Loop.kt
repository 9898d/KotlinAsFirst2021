 * Найти число Фибоначчи из ряда 1, 1, 2, 3, 5, 8, 13, 21, ... с номером n.
 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
//    return when (n) {
//        1 -> 1
//        2 -> 1
//        else -> fib(n - 2) + fib(n - 1)
//    }
    var nomer = n
    var x = 1
    var xsled = 1
    while (nomer > 2) {
        val xpred = x
        x = xsled
        xsled = xpred + x
        nomer -= 1
    }
    return xsled
}

